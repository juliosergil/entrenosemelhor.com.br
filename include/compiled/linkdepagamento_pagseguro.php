  </p>

  <p style="margin:.35rem 0">
    Para criar um link de pagamento de um plano específico (ex.: PagSeguro), acesse a sua conta
    <a href="https://www.pagseguro.com.br" target="_blank" rel="noopener">www.pagseguro.com.br</a>,
    no menu esquerdo clique em <b>Vendas &gt; Link de Pagamento</b>, informe nome do plano e valor,
    salve, copie o link gerado e cole no campo do plano no Vipmin. Repita para cada plano.
  </p>

  <p style="margin:.35rem 0">
